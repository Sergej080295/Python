'''
Задача №4:
Определите, можно ли от шоколадки размером a × b долек отломить c долек, если разрешается 
сделать один разлом по прямой между дольками (то есть разломить шоколадку на два прямоугольника).

Выведите yes или no соответственно.
'''

def can_break_chocolate(a, b, c):
    # Проверяем, если общее количество долек (a*b) меньше или равно количеству отломляемых долек (c)
    if a * b >= c:
        # Проверяем, можем ли отломить c долек, разбивая шоколадку на два прямоугольника
        if c % a == 0 or c % b == 0:
            return "yes"
        else:
            return "no"
    else:
        return "no"

# Пример использования функции
a = int(input("Введите количество долек по ширине: "))
b = int(input("Введите количество долек по высоте: "))
c = int(input("Введите количество отламываемых долек: "))

print(can_break_chocolate(a, b, c))