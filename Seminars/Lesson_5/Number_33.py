'''
Задача №33. 
Хакер Василий получил доступ к классному журналу и хочет заменить все свои минимальные оценки на
максимальные. Напишите программу, которая заменяет оценки Василия, но наоборот: все максимальные – на минимальные.

Input: 5 -> 1 3 3 3 4
Output: 1 3 3 3 1
'''

# Ввод оценок
grades = list(map(int, input("Введите оценки через пробел: ").split()))

# Находим минимальную и максимальную оценки
min_grade = min(grades)
max_grade = max(grades)

# Заменяем максимальные оценки на минимальные
for i in range(len(grades)):
    if grades[i] == max_grade:
        grades[i] = min_grade

# Вывод результата
print(" ".join(map(str, grades)))