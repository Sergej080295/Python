'''
Задача №39. 
Даны два массива чисел. Требуется вывести те элементы первого массива (в том порядке, 
в каком они идут в первом массиве), которых нет во втором массиве. Пользователь вводит 
число N - количество элементов в первом массиве, затем N чисел - элементы массива. Затем 
число M - количество элементов во втором массиве. Затем элементы второго массива

Ввод:                
7
3 1 3 4 2 4 12
6
4 15 43 1 15 1

Вывод:
3 3 2 12
(каждое число вводится с новой строки)
'''

def find_missing_elements(arr1, arr2):
    # Создаём пустой список, в который будем добавлять элементы из arr1,
    # которых нет в arr2
    missing_elements = []

    # Проходим по каждому элементу в arr1
    for elem in arr1:
        # Если элемент не содержится в arr2, добавляем его в missing_elements
        if elem not in arr2:
            missing_elements.append(elem)

    # Возвращаем список missing_elements
    return missing_elements

# Ввод значений от пользователя
n = int(input("Введите количество элементов первого массива: "))
arr1 = [int(input("Введите элемент первого массива: ")) for _ in range(n)]

m = int(input("Введите количество элементов второго массива: "))
arr2 = [int(input("Введите элемент второго массива: ")) for _ in range(m)]

# Находим элементы первого массива, которых нет во втором массиве
missing_elements = find_missing_elements(arr1, arr2)

# Выводим элементы в одну строку
print(*missing_elements)
