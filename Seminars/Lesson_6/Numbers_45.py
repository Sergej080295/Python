'''
Задача №45.
Два различных натуральных числа n и m называются дружественными, если сумма делителей числа n
(включая 1, но исключая само n) равна числу m и наоборот. Например, 220 и 284 – дружественные числа.
По данному числу k выведите все пары дружественных чисел, каждое из которых не превосходит k. Программа
получает на вход одно натуральное число k, не превосходящее 105. Программа должна вывести все пары 
дружественных чисел, каждое из которых не превосходит k. Пары необходимо выводить по одной в строке, разделяя 
пробелами. Каждая пара должна быть выведена только один раз (перестановка чисел новую пару не дает).

Ввод: 
300
Вывод:
220 284
'''

# Функция вычисляет сумму всех делителей числа n, исключая само число.
def sum_of_divisors(n):
    divisors_sum = 0
    for i in range(1, n):
        if n % i == 0:
            divisors_sum += i
    return divisors_sum

# Функция находит все пары дружественных чисел, каждое из которых не превосходит k.
def find_friendly_numbers(k):
    friendly_pairs = []
    # Перебираем все числа от 1 до k
    for n in range(1, k+1):
        m = sum_of_divisors(n)
        # Проверяем, является ли число m делителем числа n
        if n < m <= k and sum_of_divisors(m) == n:
            friendly_pairs.append((n, m))
    return friendly_pairs

# Ввод значения k от пользователя
k = int(input("Введите число k: "))

# Находим все пары дружественных чисел, каждое из которых не превосходит k
result = find_friendly_numbers(k)

# Выводим результат
for pair in result:
    print(pair[0], pair[1])
